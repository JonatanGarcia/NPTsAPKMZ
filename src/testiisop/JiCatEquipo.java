    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here:
        if(idEquipo!=0){
            if (Validar()){  
                CatCompania catCompania=new CatCompania();
                catCompania.setIdCatCompania(((Multivalor)CmbCompania.getSelectedItem()).getIndice());
                
                CatEquipo catEquipo=new CatEquipo(idEquipo, catCompania, JtxtEquipo.getText());
              
                boolean respuesta=CatEquipoDAO.setUpdateCaTEquipo(catEquipo);
                 if (respuesta){
                    JOptionPane.showMessageDialog(rootPane, "Información Modificada con Éxito", Menu.nombreSistema, JOptionPane.INFORMATION_MESSAGE);
                    Limpiar();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Error al intentar modificar la información", Menu.nombreSistema, JOptionPane.ERROR_MESSAGE);
                }
                catEquipo=null;
            }
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

