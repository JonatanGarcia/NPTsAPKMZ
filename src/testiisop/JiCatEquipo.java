    public boolean Validar(){
        if (JtxtEquipo.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane,"Equipo vacio",Menu.nombreSistema, JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    public void Limpiar(){
        JtxtBusqueda.setText("");
        JtxtEquipo.setText("");
        idEquipo=0;
        Llenar(true,"");
    }
    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        if (Validar()){
            Multivalor valor=(Multivalor)CmbCompania.getSelectedItem();
            int idCompania=valor.getIndice();
            boolean respuesta= CatEquipoDAO.setNewCatEquipo(JtxtEquipo.getText(),idCompania);
            if(respuesta){
                JOptionPane.showMessageDialog(rootPane, "Información Agregada con Éxito", Menu.nombreSistema, JOptionPane.INFORMATION_MESSAGE);
                Limpiar();
            }else{
                JOptionPane.showMessageDialog(rootPane, "Error al Agregar la Información", Menu.nombreSistema, JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount()==2){
            Multivalor valorEquipo=null;
            Multivalor combo=null;
            int fila=jTable1.rowAtPoint(evt.getPoint());
            if(fila>-1){
                valorEquipo=(Multivalor)jTable1.getValueAt(fila, 0);
                combo=(Multivalor)jTable1.getValueAt(fila, 1);
                JtxtEquipo.setText(valorEquipo.getValor());
                idEquipo=valorEquipo.getIndice();
                CmbCompania.setSelectedItem(combo);
                valorEquipo=null;
                combo=null;
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

